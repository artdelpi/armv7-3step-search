.text

.macro FIM
    mov r0, #0x18
    ldr r1, =0x20026
    svc 0x123456
.endm

.macro gray2rgb565
    // r0 = 1 byte gray, return r0 = rgb565 gray
    push { r1, r2 }
    lsr r1, r0, #3
    lsl r1, r1, #11
    lsr r2, r0, #2
    lsl r2, r2, #5
    orr r1, r1, r2
    orr r0, r1, r0, lsr #3
    pop { r1, r2 }
.endm

.global main

main:
    mov r0, #BLACK
    bl  limpaTela

    ldr r0, =imagem
    mov r1, #64
    mov r2, #64
    bl  drawGrayBlk

    @ testa blocoA
    ldr  r0, =blocoA
    mov  r1, #0          @ x_inicial = 0
    mov  r2, #0          @ y_inicial = 0
    bl   busca3p
    
    @ desenha contorno BRANCO na posição encontrada
    mov  r3, r0          @ r3 = x_encontrado
    mov  r2, r1          @ r2 = y_encontrado (FIX)
    mov  r0, #WHITE      @ r0 = cor
    mov  r1, r3          @ r1 = x_encontrado
    bl   drawRectOutline8


    @ testa blocoB   
	
    // desenha outline azul de gabarito
    mov  r0, #BLUE
    mov  r1, #8
    mov  r2, #32
    bl   drawRectOutline8

    @ inicia a busca
    ldr  r0, =blocoB
    mov  r1, #16         @ x_inicial = centro
    mov  r2, #32         @ y_inicial = centro
    bl   busca3p

    @ desenha contorno VERMELHO na posição encontrada
    mov  r3, r0          @ r3 = x_encontrado
    mov  r2, r1          @ r2 = y_encontrado (FIX)
    mov  r0, #RED        @ r0 = cor
    mov  r1, r3          @ r1 = x_encontrado
    bl   drawRectOutline8

    b .

setGrayPix:
    // r0: gray (1B), r1: x, r2: y
    gray2rgb565
    push {r3}
    ldr  r3, =VGA
    add  r3, r3, r2, lsl #LOG2_BYTES_PER_ROW
    add  r3, r3, r1, lsl #LOG2_BYTES_PER_PIXEL
    strh r0, [r3]
    pop  {r3}
    bx   lr

drawGrayBlk:
    // r0=&pixel, r1=dx_blk, r2=dy_blk
    push {r4-r8, lr}
    mov  r4, r0      // &pixel
    mov  r5, r1      // dx_blk
    mul  r6, r1, r2  // total pixels
    mov  r7, #0      // x_vga
    mov  r8, #0      // y_vga
dLoop:
    cmp  r6, #0
    ble  retGP
    add  r6, #-1
    ldrb r0, [r4], #1
    mov  r1, r7
    mov  r2, r8
    bl   setGrayPix
    add  r7, #1
    cmp  r7, r5
    blt  dLoop
    mov  r7, #0
    add  r8, #1
    b    dLoop
retGP:
    pop  {r4-r8, lr}
    bx   lr

limpaTela:  // r0 = COR
    mov r1, r0
    ldr r0, =VGA              @ Base do framebuffer
    mov r2, #0                @ Contador de pixels
    mov r3, #(240 * 512)      @ Número total de pixels
loop:
    strh r1, [r0], #2         @ Escreve a cor e avança
    add  r2, r2, #1
    cmp  r2, r3
    bne  loop
    bx   lr

// Desenha borda 8x8 em (x,y) com cor RGB565
drawRectOutline8:
    push {r4-r8, lr}
    mov  r4, r0      @ cor
    mov  r5, r1      @ x
    mov  r6, r2      @ y

    @ topo: (x .. x+7, y)
    mov  r7, #0
top_line:
    mov  r0, r4
    add  r1, r5, r7
    mov  r2, r6
    bl   setPix565
    add  r7, r7, #1
    cmp  r7, #8
    blt  top_line

    @ base: (x .. x+7, y+7)
    mov  r7, #0
    add  r2, r6, #7
bot_line:
    mov  r0, r4
    add  r1, r5, r7
    bl   setPix565
    add  r7, r7, #1
    cmp  r7, #8
    blt  bot_line

    @ laterais
    mov  r7, #0
sides:
    mov  r0, r4
    mov  r1, r5
    add  r2, r6, r7
    bl   setPix565

    mov  r0, r4
    add  r1, r5, #7
    add  r2, r6, r7
    bl   setPix565

    add  r7, r7, #1
    cmp  r7, #8
    blt  sides

    pop  {r4-r8, lr}
    bx   lr

setPix565:
    // r0: cor RGB565, r1: x, r2: y
    push {r3}
    ldr  r3, =VGA
    add  r3, r3, r2, lsl #LOG2_BYTES_PER_ROW
    add  r3, r3, r1, lsl #LOG2_BYTES_PER_PIXEL
    strh r0, [r3]
    pop  {r3}
    bx   lr

.global sad
sad:
    push {r1, r2, r4-r11, lr}
    mov     r5, r0              // r5 = pontr bloco
    ldr     r3, =imagem         // r3 = base da imagem

    lsl     r4, r2, #6          // r4 = y * 64
    add     r4, r4, r1          // r4 = y*64 + x
    add     r6, r3, r4          // r6 = ptr imagem(y,x)

    mov     r9, #0              // acumulador do SAD
    mov     r7, #N              // linhas = 8
1:  mov     r8, #N              // colunas = 8
2:  ldrb    r0, [r5], #1        // bloco++
    ldrb    r1, [r6], #1        // img++
    subs    r0, r0, r1
    rsbmi   r0, r0, #0          // |diff|
    add     r9, r9, r0
    subs    r8, r8, #1
    bne     2b

    add     r6, r6, #(dx_img - N)   // avança pra próxima linha
    subs    r7, r7, #1
    bne     1b

    mov     r0, r9              // retorno = SAD
    pop     {r1, r2, r4-r11, lr}
    bx      lr

.global busca3p
busca3p:
    push    {r4-r11, lr}
    mov     r4, r0          // r4 = &bloco
    mov     r5, r1          // r5 = x centro
    mov     r6, r2          // r6 = y centro

    // passos: 8, 4, 2
    mov     r7, #8          // passo = 8
    
passo_loop:
    // analisa ponto central
    mov     r0, r4
    mov     r1, r5
    mov     r2, r6
    bl      sad
    mov     r10, r0         // min_sad
    mov     r8, r5          // best_x
    mov     r9, r6          // best_y

    // varre 3x3
    mov     r11, #3         // 3 linhas
    sub     r2, r6, r7      // y = centro - passo
    
loop_linha:
    mov     r12, #3         // 3 colunas
    sub     r1, r5, r7      // x = centro - passo
    
loop_coluna:
    // calcula SAD pra (x, y)
    mov     r0, r4
    bl      sad
    
    // atualiza se for menor
    cmp     r0, r10
    bge     next_col
    mov     r10, r0
    mov     r8, r1
    mov     r9, r2
    
next_col:
    // próxima coluna
    add     r1, r1, r7
    subs    r12, r12, #1
    bne     loop_coluna
    
    // próxima linha  
    add     r2, r2, r7
    subs    r11, r11, #1
    bne     loop_linha

    // atualiza centro
    mov     r5, r8
    mov     r6, r9

    // próximo passo
    lsrs    r7, r7, #1      // passo /= 2
    bne     passo_loop      // continua enquanto passo != 0

    mov     r0, r5          // retorna x
    mov     r1, r6          // retorna y
    pop     {r4-r11, lr}
    bx      lr

